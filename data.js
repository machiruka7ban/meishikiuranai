  const me=E[dk],my=Y[dk],te=E[ti],ty=Y[ti];
  const same=(my===ty);
  const r={"木→木":"same","木→火":"gen","木→土":"ctrl","木→金":"ctrlby","木→水":"genby","火→火":"same","火→土":"gen","火→金":"ctrl","火→水":"ctrlby","火→木":"genby","土→土":"same","土→金":"gen","土→水":"ctrl","土→木":"ctrlby","土→火":"genby","金→金":"same","金→水":"gen","金→木":"ctrl","金→火":"ctrlby","金→土":"genby","水→水":"same","水→木":"gen","水→火":"ctrl","水→土":"ctrlby","水→金":"genby"}[`${me}→${te}`];
  if(r==="same")   return same?"比肩":"劫財";
