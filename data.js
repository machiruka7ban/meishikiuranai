// ========================================
// 通変星計算関数
// ========================================
function getTsuhensei(daykan_idx, target_idx) {
  const ELEMENTS = ["木","木","火","火","土","土","金","金","水","水"];
  const YIN_YANG  = ["陽","陰","陽","陰","陽","陰","陽","陰","陽","陰"];
  const myElem = ELEMENTS[daykan_idx];
  const myYY   = YIN_YANG[daykan_idx];
  const tgElem = ELEMENTS[target_idx];
  const tgYY   = YIN_YANG[target_idx];
  const sameYY = (myYY === tgYY);
  const relMap = {
    "木→木":"same","木→火":"gen","木→土":"ctrl","木→金":"ctrlby","木→水":"genby",
    "火→火":"same","火→土":"gen","火→金":"ctrl","火→水":"ctrlby","火→木":"genby",
    "土→土":"same","土→金":"gen","土→水":"ctrl","土→木":"ctrlby","土→火":"genby",
    "金→金":"same","金→水":"gen","金→木":"ctrl","金→火":"ctrlby","金→土":"genby",
    "水→水":"same","水→木":"gen","水→火":"ctrl","水→土":"ctrlby","水→金":"genby",
  };
  const rel = relMap[`${myElem}→${tgElem}`];
  if (rel === "same")   return sameYY ? "比肩" : "劫財";
  if (rel === "gen")    return sameYY ? "食神" : "傷官";
  if (rel === "ctrl")   return sameYY ? "偏財" : "正財";
  if (rel === "ctrlby") return sameYY ? "偏官" : "正官";
  if (rel === "genby")  return sameYY ? "偏印" : "印綬";
  return "比肩";
}

