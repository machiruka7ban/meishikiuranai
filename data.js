function getFortuneRank(score) {
  return FORTUNE_RANKS.find(r => score >= r.minScore) || FORTUNE_RANKS[FORTUNE_RANKS.length-1];
}

/**
 * 相性スコアを計算する
 * @param {string} myElem 私の日干の五行
 * @param {string} theirElem 相手の日干の五行
 * @param {object} myBalance 私の五行バランス
 * @param {object} theirBalance 相手の五行バランス
 * @returns {number} 0〜100のスコア
 */
function calcAishoScore(myElem, theirElem, myBalance, theirBalance) {
  const rel = GOGYOU_RELATION[myElem]?.[theirElem] || "比和";
  let score = RELATION_SCORE[rel] || 70;

  // 相手が自分の不足を補う五行を持っていると加点
  const order = ["木","火","土","金","水"];
  const myWeak  = order.reduce((a,b) => (myBalance[a]||0) < (myBalance[b]||0) ? a : b);
  if (theirBalance[myWeak] > 20) score = Math.min(100, score + 8);

  // 五行バランスが似ているほど安定感を加点
  let diff = 0;
  order.forEach(e => { diff += Math.abs((myBalance[e]||0) - (theirBalance[e]||0)); });
  if (diff < 40) score = Math.min(100, score + 5);

  return Math.min(100, Math.max(0, score));
}

